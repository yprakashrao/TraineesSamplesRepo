public class Operator {
    public static void main(String [] args){
        int sum1 = 100+50;
        int sum2 = sum1+50;
        int sum3 = sum2-50;
        int sum4 = sum3*2;
        int sum5 = sum4/2;
        int sum6 = sum5%2;

        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(sum3);
        System.out.println(sum4);
        System.out.println(sum5);
        System.out.println(sum6);
    }

}
